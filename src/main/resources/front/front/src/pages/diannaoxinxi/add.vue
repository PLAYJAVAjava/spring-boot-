      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='diannaomingcheng'){
              this.ruleForm.diannaomingcheng = obj[o];
              this.ro.diannaomingcheng = true;
              continue;
            }
            if(o=='diannaoleixing'){
              this.ruleForm.diannaoleixing = obj[o];
              this.ro.diannaoleixing = true;
              continue;
