              this.ruleForm.xiangqingcanshu = obj[o];
              this.ro.xiangqingcanshu = true;
              continue;
            }
            if(o=='diannaotupian'){
              this.ruleForm.diannaotupian = obj[o].split(",")[0];
              this.ro.diannaotupian = true;
              continue;
            }
            if(o=='onelimittimes'){
              this.ruleForm.onelimittimes = obj[o];
              this.ro.onelimittimes = true;
